const dictionary: Record<Language, Record<TranslationKey, string>> = { ru, en };

export const locale = derived(settings, ($settings) =>
  $settings.language === "en" ? "en" : "ru"
);

export const tr = derived(locale, ($locale) => {
  return (key: TranslationKey, vars?: Record<string, string | number>) => {
    let value = dictionary[$locale][key] ?? dictionary.ru[key] ?? key;
    if (!vars) return value;
    for (const [k, v] of Object.entries(vars)) {
      value = value.replaceAll(`{${k}}`, String(v));
    }
    return value;
  };
});
